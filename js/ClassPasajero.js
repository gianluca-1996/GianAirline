class Pasajero{
    nombre
    apellido
    dni
    direccion
    edad
    email

    constructor(nombre, apellido, dni, direccion, edad, email){
        this.nombre = nombre,
        this.apellido = apellido,
        this.dni = dni
        this.direccion = direccion
        this.edad = edad
        this.email = email
    }
}



    
    

